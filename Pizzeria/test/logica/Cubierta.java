package logica;

public class Cubierta {
	private Integer idCubieta;
	private String nombre;
	
	public Cubierta(String nombre) {
		this.nombre = nombre;
	}

	public Integer getIdCubieta() {
		return idCubieta;
	}
	public void setIdCubieta(Integer idCubieta) {
		this.idCubieta = idCubieta;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
}
